package com.linov.psikotes.pojo;

public class PojoQuestReport {
	
	private String questionTitle;
	private String totalQuestion;
	private String totalCorrect;
	private String percentation;
	
	public String getQuestionTitle() {
		return questionTitle;
	}
	
	public void setQuestionTitle(String questionTitle) {
		this.questionTitle = questionTitle;
	}
	
	public String getTotalQuestion() {
		return totalQuestion;
	}
	
	public void setTotalQuestion(String totalQuestion) {
		this.totalQuestion = totalQuestion;
	}
	
	public String getTotalCorrect() {
		return totalCorrect;
	}
	
	public void setTotalCorrect(String totalCorrect) {
		this.totalCorrect = totalCorrect;
	}
	
	public String getPercentation() {
		return percentation;
	}
	
	public void setPercentation(String percentation) {
		this.percentation = percentation;
	}
	
}
